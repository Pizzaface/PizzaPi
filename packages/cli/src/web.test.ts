const FULL_COMPOSE = `services:
  redis:
    image: redis:7-alpine
  server:
    ports:
      - "8080:7492"
    environment:
      - VAPID_PUBLIC_KEY=BRealPublicKeyABC123
      - VAPID_PRIVATE_KEY=RealPrivateKeyXYZ789
      - VAPID_SUBJECT=mailto:ops@example.com
      - BETTER_AUTH_SECRET=RealAuthSecret123
      - PIZZAPI_EXTRA_ORIGINS=https://my-tailscale.ts.net`;

describe("extractSettingsFromCompose", () => {
    test("extracts all settings from a fully configured compose file", () => {
        const result = extractSettingsFromCompose(FULL_COMPOSE);
        expect(result).toEqual({
            vapid: { publicKey: "BRealPublicKeyABC123", privateKey: "RealPrivateKeyXYZ789" },
            vapidSubject: "mailto:ops@example.com",
            extraOrigins: "https://my-tailscale.ts.net",
            port: 8080,
            betterAuthSecret: "RealAuthSecret123",
        });
    });

    test("returns empty object for template placeholders", () => {
        const template = FULL_COMPOSE
            .replace("BRealPublicKeyABC123", "{{VAPID_PUBLIC_KEY}}")
            .replace("RealPrivateKeyXYZ789", "{{VAPID_PRIVATE_KEY}}")
            .replace("mailto:ops@example.com", "{{VAPID_SUBJECT}}")
            .replace("RealAuthSecret123", "{{BETTER_AUTH_SECRET}}")
            .replace("https://my-tailscale.ts.net", "{{EXTRA_ORIGINS}}");
        const result = extractSettingsFromCompose(template);
        expect(result.vapid).toBeUndefined();
        expect(result.vapidSubject).toBeUndefined();
        expect(result.betterAuthSecret).toBeUndefined();
        expect(result.extraOrigins).toBeUndefined();
    });

    test("returns empty object when nothing is present", () => {
        const result = extractSettingsFromCompose("services:\n  redis:\n    image: redis");
        expect(result).toEqual({});
    });

    test("handles trailing whitespace in values", () => {
        const content = "VAPID_PUBLIC_KEY=KeyA  \nVAPID_PRIVATE_KEY=KeyB  \n";
        const result = extractSettingsFromCompose(content);
        expect(result.vapid).toEqual({ publicKey: "KeyA", privateKey: "KeyB" });
    });

    test("skips commented-out PIZZAPI_EXTRA_ORIGINS", () => {
        const content = `    environment:
      - VAPID_PUBLIC_KEY=Key1
      - VAPID_PRIVATE_KEY=Key2
      # - PIZZAPI_EXTRA_ORIGINS=`;
        const result = extractSettingsFromCompose(content);
        expect(result.extraOrigins).toBeUndefined();
    });

    test("extracts port from host:container mapping", () => {
        const content = `    ports:\n      - "9000:7492"`;
        const result = extractSettingsFromCompose(content);
        expect(result.port).toBe(9000);
    });

    test("parses YAML mapping syntax for proxy settings (double-quoted)", () => {
        // This is the format shown in self-hosting docs and produced by standard
        // YAML tools — the compose.override.yml uses mapping form, not list form.
        const content = `services:
  server:
    environment:
      PIZZAPI_TRUST_PROXY: "true"
      PIZZAPI_PROXY_DEPTH: "1"`;
        const result = extractSettingsFromCompose(content);
        expect(result.trustProxy).toBe(true);
        expect(result.proxyDepth).toBe(1);
    });

    test("parses YAML mapping syntax for proxy settings (single-quoted)", () => {
        const content = `services:
  server:
    environment:
      PIZZAPI_TRUST_PROXY: 'false'
      PIZZAPI_PROXY_DEPTH: '2'`;
        const result = extractSettingsFromCompose(content);
        expect(result.trustProxy).toBe(false);
        expect(result.proxyDepth).toBe(2);
    });

    test("parses YAML mapping syntax for proxy settings (unquoted)", () => {
        const content = `services:
  server:
    environment:
      PIZZAPI_TRUST_PROXY: true
      PIZZAPI_PROXY_DEPTH: 1`;
        const result = extractSettingsFromCompose(content);
        expect(result.trustProxy).toBe(true);
        expect(result.proxyDepth).toBe(1);
    });

    test("strips inline YAML comments from proxy settings", () => {
        const content = `services:
  server:
    environment:
      PIZZAPI_TRUST_PROXY: "true" # Caddy
      PIZZAPI_PROXY_DEPTH: "1" # trusted hop count`;
        const result = extractSettingsFromCompose(content);
        expect(result.trustProxy).toBe(true);
        expect(result.proxyDepth).toBe(1);
    });

    test("parses YAML mapping syntax for all env vars", () => {
        const content = `services:
  server:
    ports:
      - "8080:7492"
    environment:
      VAPID_PUBLIC_KEY: "MapPubKey123"
      VAPID_PRIVATE_KEY: "MapPrivKey456"
      VAPID_SUBJECT: "mailto:map@example.com"
      BETTER_AUTH_SECRET: "MapSecret789"
      PIZZAPI_EXTRA_ORIGINS: "https://map.example.com"
      PIZZAPI_TRUST_PROXY: "true"
      PIZZAPI_PROXY_DEPTH: "2"`;
        const result = extractSettingsFromCompose(content);
        expect(result.vapid).toEqual({ publicKey: "MapPubKey123", privateKey: "MapPrivKey456" });
        expect(result.vapidSubject).toBe("mailto:map@example.com");
        expect(result.betterAuthSecret).toBe("MapSecret789");
        expect(result.extraOrigins).toBe("https://map.example.com");
        expect(result.port).toBe(8080);
        expect(result.trustProxy).toBe(true);
        expect(result.proxyDepth).toBe(2);
    });

    test("list form still takes priority over mapping form", () => {
        // When both forms exist, list form regex matches first (realistic edge case)
        const content = `    environment:
      - PIZZAPI_TRUST_PROXY=true
      PIZZAPI_TRUST_PROXY: "false"`;
        const result = extractSettingsFromCompose(content);
        expect(result.trustProxy).toBe(true);
    });
});

