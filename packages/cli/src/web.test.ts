describe("resolveComposeMode", () => {
    test("returns build mode fields when image is empty", () => {
        expect(resolveComposeMode("/repo", { image: "", imageTag: "latest" })).toEqual({
            buildBlock: '    build:\n      context: /repo\n      dockerfile: Dockerfile\n      args:\n        PREBUILT_UI: "{{PREBUILT_UI}}"\n        UI_DIST_HASH: "{{UI_DIST_HASH}}"\n',
            imageLine: "",
            hubImage: "local-build",
            hubVersion: "local",
        });
    });

    test("returns image mode fields with if_not_present for pinned tags", () => {
        expect(resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi", imageTag: "0.1.32" })).toEqual({
            buildBlock: "",
            imageLine: "    image: ghcr.io/acme/pizzapi:0.1.32\n    pull_policy: if_not_present\n",
            hubImage: "ghcr.io/acme/pizzapi:0.1.32",
            hubVersion: "0.1.32",
        });
    });

    test("uses pull_policy: always for mutable tags like latest", () => {
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi", imageTag: "latest" });
        expect(result.imageLine).toContain("pull_policy: always");
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:latest");
    });

    test("uses pull_policy: always for mutable tag 'main'", () => {
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi", imageTag: "main" });
        expect(result.imageLine).toContain("pull_policy: always");
    });

    test("handles digest-pinned image references without appending tag", () => {
        const digest = "ghcr.io/acme/pizzapi@sha256:abc123def456";
        const result = resolveComposeMode("/repo", { image: digest, imageTag: "latest" });
        expect(result.imageLine).toContain(`image: ${digest}`);
        expect(result.imageLine).not.toContain(":latest");
        expect(result.imageLine).toContain("pull_policy: if_not_present");
        expect(result.hubImage).toBe(digest);
        expect(result.hubVersion).toBe("sha256:abc123def456");
    });

    test("preserves at least 12 hex chars in digest hubVersion for long digests", () => {
        const longDigest = "ghcr.io/acme/pizzapi@sha256:abcdef0123456789abcdef0123456789abcdef0123456789abcdef0123456789";
        const result = resolveComposeMode("/repo", { image: longDigest, imageTag: "latest" });
        expect(result.hubVersion).toBe("sha256:abcdef012345");
    });

    test("defaults to latest tag when imageTag is empty", () => {
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi", imageTag: "" });
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:latest");
        expect(result.imageLine).toContain("pull_policy: always");
    });

    test("uses embedded tag when image already contains a tag (P2 regression)", () => {
        // "ghcr.io/acme/pizzapi:0.1.32" must NOT become "ghcr.io/acme/pizzapi:0.1.32:latest"
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi:0.1.32", imageTag: "latest" });
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:0.1.32");
        expect(result.imageLine).not.toContain("0.1.32:latest");
        expect(result.imageLine).toContain("pull_policy: if_not_present");
        expect(result.hubImage).toBe("ghcr.io/acme/pizzapi:0.1.32");
        expect(result.hubVersion).toBe("0.1.32");
    });

    test("embedded mutable tag (latest) uses pull_policy: always", () => {
        // An operator who writes "ghcr.io/acme/pizzapi:latest" in the image field intends to
        // track the latest image.  Even though the tag is embedded in the ref, "latest" is a
        // moving pointer and should be re-pulled on restart so the deployment stays current.
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi:latest", imageTag: "" });
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:latest");
        expect(result.imageLine).toContain("pull_policy: always");
    });

    test("honors explicit non-default imageTag when image has embedded tag (P2 regression)", () => {
        // If an operator pasted a full ref "ghcr.io/acme/pizzapi:0.1.32" as the
        // image setting and later runs `pizza web --tag 0.1.33`, the explicit tag
        // must override the embedded one so the upgrade takes effect.
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi:0.1.32", imageTag: "0.1.33" });
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:0.1.33");
        expect(result.imageLine).not.toContain("0.1.32");
        expect(result.hubVersion).toBe("0.1.33");
        expect(result.hubImage).toBe("ghcr.io/acme/pizzapi:0.1.33");
    });

    test("does not override embedded tag when imageTag is the default 'latest'", () => {
        // imageTag defaults to "latest" — an operator who pasted
        // "ghcr.io/acme/pizzapi:0.1.32" without touching imageTag should NOT
        // silently switch to :latest.  The embedded tag wins when imageTag is
        // still the default.
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi:0.1.32", imageTag: "latest" });
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:0.1.32");
        expect(result.hubVersion).toBe("0.1.32");
    });

    test("honors explicit mutable imageTag override when image has embedded semver tag", () => {
        // `pizza web --tag main` on an image ref with an embedded semver tag should
        // switch to the mutable :main pointer.
        const result = resolveComposeMode("/repo", { image: "ghcr.io/acme/pizzapi:0.1.32", imageTag: "main" });
        expect(result.imageLine).toContain("image: ghcr.io/acme/pizzapi:main");
        expect(result.imageLine).toContain("pull_policy: always");
        expect(result.hubVersion).toBe("main");
    });

    test("returns build mode when image is whitespace-only (P3 regression)", () => {
        // A whitespace-only image value must NOT flip into image mode
        const result = resolveComposeMode("/repo", { image: "   ", imageTag: "latest" });
        expect(result.buildBlock).toContain("context: /repo");
        expect(result.imageLine).toBe("");
        expect(result.hubImage).toBe("local-build");
    });
});

describe("normalizeImageRepoForExplicitTag", () => {
    test("strips an embedded tag so an explicit imageTag can take over", () => {
        expect(normalizeImageRepoForExplicitTag("ghcr.io/acme/pizzapi:0.1.32")).toBe("ghcr.io/acme/pizzapi");
    });

    test("strips digest from digest-pinned refs so explicit tag can be applied", () => {
        const digestRef = "ghcr.io/acme/pizzapi@sha256:abcdef0123456789";
        expect(normalizeImageRepoForExplicitTag(digestRef)).toBe("ghcr.io/acme/pizzapi");
    });

    test("strips both digest and embedded tag from digest+tag refs", () => {
        // e.g. ghcr.io/acme/pizzapi:0.1.32@sha256:abc... — both must be stripped
        // so that --tag latest is not silently ignored by resolveComposeMode.
        const digestTagRef = "ghcr.io/acme/pizzapi:0.1.32@sha256:abcdef0123456789";
        expect(normalizeImageRepoForExplicitTag(digestTagRef)).toBe("ghcr.io/acme/pizzapi");
    });

    test("leaves plain repos unchanged", () => {
        expect(normalizeImageRepoForExplicitTag("ghcr.io/acme/pizzapi")).toBe("ghcr.io/acme/pizzapi");
    });
});

